package com.company;

public class L4_literals {
    public static void main(String[] args) {
        byte age = 34 ;
        short marks = 87 ;
        int roll = 123 ;
        long phone = 1234567890123L ;
        float pi = 3.14f ;
        double e = 2.718281828459045 ;
        char ch = 'A' ;
        boolean flag = true ;
        String str = " Arnab" ;
        System.out.println("The value of age is : " + age);
        System.out.println("The value of marks is : " + marks);
        System.out.println("The value of roll is : " + roll);
        System.out.println("The value of phone is : " + phone);
        System.out.println("The value of pi is : " + pi);
        System.out.println("The value of e is : " + e);
        System.out.println("The value of ch is : " + ch);
        System.out.println("The value of flag is : " + flag);
        System.out.println("The value of str is : " + str);



    }
}
